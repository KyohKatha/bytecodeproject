public class Tag {
    private String id;
    private double idParticipante;
    private double idEvento;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public double getIdEvento() {
        return idEvento;
    }

    public void setIdEvento(double idEvento) {
        this.idEvento = idEvento;
    }

    public double getIdParticipante() {
        return idParticipante;
    }

    public void setIdParticipante(double idParticipante) {
        this.idParticipante = idParticipante;
    }

    
}
